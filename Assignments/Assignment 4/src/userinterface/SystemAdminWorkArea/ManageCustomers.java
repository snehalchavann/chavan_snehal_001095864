    private void CustomerList() {
        ArrayList<UserAccount> userAccountList = system.getUserAccountDirectory().getUserAccountList();
        ArrayList<Customer> customerList = system.getCustomerDirectory().getCustomerList();
         model = new DefaultTableModel();
        jTable_customerList.setModel(model);
        model.addColumn("Name");
        model.addColumn("Contact Number");
        model.addColumn("Address");
        for(int i=0;i<customerList.size();i++){
                model.addRow(new Object[]{
                    customerList.get(i).getCustomerName(),
                    customerList.get(i).getContactNumber(),
                    customerList.get(i).getAddress()
                });
        }
    }
