    private void jTable_customerListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_customerListMouseClicked
        // TODO add your handling code here:
        Vector elementAt = model.getDataVector().elementAt(jTable_customerList.getSelectedRow());
        jTextFieldCustomerName.setText((String) elementAt.get(0));
        jTextFieldCustomerContact.setText((String) elementAt.get(1));
        jTextFieldCustomerAddress.setText((String) elementAt.get(2));
        ArrayList<UserAccount> userAccountList = system.getUserAccountDirectory().getUserAccountList();
        for(int i=0;i<userAccountList.size();i++){
            if(userAccountList.get(i).getEmployee().getName().equals(jTextFieldCustomerName.getText())){
                jTextFieldCustomerusername.setText(userAccountList.get(i).getUsername());
                jPasswordFieldCustomer.setText(userAccountList.get(i).getPassword());
            }
        }
    }//GEN-LAST:event_jTable_customerListMouseClicked

    private void jButtonDeleteCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteCustomerActionPerformed
        // TODO add your handling code here:
        String custName = jTextFieldDelCust.getText();
        ArrayList<Customer> customerList = system.getCustomerDirectory().getCustomerList();
        for(int i=0;i<customerList.size();i++){
            if(customerList.get(i).getCustomerName().equals(custName)){
                customerList.remove(i);
                CustomerList();
                break;
            }
            
        }
        for(int i=0;i<system.getUserAccountDirectory().getUserAccountList().size();i++){
            if(system.getUserAccountDirectory().getUserAccountList().get(i).getEmployee().getName().equals(custName)){
                system.getUserAccountDirectory().getUserAccountList().remove(i);
                
            }
            if(system.getEmployeeDirectory().getEmployeeList().get(i).getName().equals(custName)){
                    system.getEmployeeDirectory().getEmployeeList().remove(i);
            }
        }
        jTextFieldDelCust.setText("");
        JOptionPane.showMessageDialog(null, "Customer Deleted.");
    }//GEN-LAST:event_jButtonDeleteCustomerActionPerformed

