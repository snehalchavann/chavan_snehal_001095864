    private void jButtonCreateCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCreateCustomerActionPerformed
        // TODO add your handling code here:
        String name = jTextFieldCustomerName.getText();
        String contact = jTextFieldCustomerContact.getText();
        String address = jTextFieldCustomerAddress.getText();
        Employee customer = system.getEmployeeDirectory().createEmployee(name);
        String username = jTextFieldCustomerusername.getText();
        String password = String.valueOf(jPasswordFieldCustomer.getPassword());
        system.getCustomerDirectory().newCustomer(name, contact, address);
        UserAccount managerAdmin = system.getUserAccountDirectory().createUserAccount(username, password, customer, new CustomerRole());
        jTextFieldCustomerName.setText("");
        jTextFieldCustomerusername.setText("");
        jPasswordFieldCustomer.setText("");
        jTextFieldCustomerContact.setText("");
        jTextFieldCustomerAddress.setText("");
        CustomerList();
        JOptionPane.showMessageDialog(null, "Customer created.");
    }//GEN-LAST:event_jButtonCreateCustomerActionPerformed

    private void jButton1upadteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1upadteActionPerformed
        // TODO add your handling code here:
        ArrayList<Customer> customerList = system.getCustomerDirectory().getCustomerList();
        ArrayList<UserAccount> userAccountList = system.getUserAccountDirectory().getUserAccountList();
        for(int i=0;i<userAccountList.size();i++){
            if(userAccountList.get(i).getEmployee().getName().equals(jTextFieldCustomerName.getText())){
                userAccountList.get(i).setPassword(String.valueOf(jPasswordFieldCustomer.getPassword()));
                userAccountList.get(i).setUsername(jTextFieldCustomerusername.getText());
            }
        }
        for(int i=0;i<customerList.size();i++){
            if(customerList.get(i).getCustomerName().equals(jTextFieldCustomerName.getText())){
                customerList.get(i).setContactNumber(jTextFieldCustomerContact.getText());
                customerList.get(i).setAddress(jTextFieldCustomerAddress.getText());
            }
        }
        CustomerList();
        jTextFieldCustomerName.setText("");
        jTextFieldCustomerusername.setText("");
        jPasswordFieldCustomer.setText("");
        jTextFieldCustomerContact.setText("");
        jTextFieldCustomerAddress.setText("");
        JOptionPane.showMessageDialog(null, "Data updated successfully.");
    }//GEN-LAST:event_jButton1upadteActionPerformed

