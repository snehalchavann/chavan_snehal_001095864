    public void viewEmployer() {
        if (employerDirectory.getEmployerlist().size() > 0) {
            for (int i = 0; i < employerDirectory.getEmployerlist().size(); i++) {

                model.addRow(new Object[]{
                    employerDirectory.getEmployerlist().get(i).getName(),
                    employerDirectory.getEmployerlist().get(i).getContactNo(),
                    employerDirectory.getEmployerlist().get(i).getCompanyAge(),
                    employerDirectory.getEmployerlist().get(i).getLocation(),
                    employerDirectory.getEmployerlist().get(i).getCompanyAssociationWithUniversity()
                });
            }
        }
    }

