        boolean flag = false;
        
        if(CompanyNameTextField.getText().isEmpty() || AssociationAgeText.getText().isEmpty() || ContactText.getText().isEmpty()
                || ageText.getText().isEmpty() || addressText.getText().isEmpty()){
            flag = true;
            JOptionPane.showMessageDialog(null, "Please enter all fields");
        }
        
        String pattern = "^[A-Za-z]+$";
        String number = "^\\d+$";
        java.util.regex.Pattern r = java.util.regex.Pattern.compile(pattern);
        java.util.regex.Pattern r1 = java.util.regex.Pattern.compile(number);
